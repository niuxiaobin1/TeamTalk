package com.mogujie.tt.imservice.entity;

public class CardInfo {

    private String card_id;
    private String card_bank_no;
    private String card_bank_name;
    private String card_account_name;
    private String card_account_number;
    private String card_default;

    public String getCard_id() {
        return card_id;
    }

    public void setCard_id(String card_id) {
        this.card_id = card_id;
    }

    public String getCard_bank_no() {
        return card_bank_no;
    }

    public void setCard_bank_no(String card_bank_no) {
        this.card_bank_no = card_bank_no;
    }

    public String getCard_bank_name() {
        return card_bank_name;
    }

    public void setCard_bank_name(String card_bank_name) {
        this.card_bank_name = card_bank_name;
    }

    public String getCard_account_name() {
        return card_account_name;
    }

    public void setCard_account_name(String card_account_name) {
        this.card_account_name = card_account_name;
    }

    public String getCard_account_number() {
        return card_account_number;
    }

    public void setCard_account_number(String card_account_number) {
        this.card_account_number = card_account_number;
    }

    public String getCard_default() {
        return card_default;
    }

    public void setCard_default(String card_default) {
        this.card_default = card_default;
    }
}

package com.mogujie.tt.imservice.entity;

public class PayOrderInfoData {

    private String b_time;
    private String amount;
    private String m_name;
    private String user_name;
    private String payee_user_name;
    private String order_sn;
    private String b_type;

    public String getB_time() {
        return b_time;
    }

    public void setB_time(String b_time) {
        this.b_time = b_time;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getM_name() {
        return m_name;
    }

    public void setM_name(String m_name) {
        this.m_name = m_name;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getPayee_user_name() {
        return payee_user_name;
    }

    public void setPayee_user_name(String payee_user_name) {
        this.payee_user_name = payee_user_name;
    }

    public String getOrder_sn() {
        return order_sn;
    }

    public void setOrder_sn(String order_sn) {
        this.order_sn = order_sn;
    }

    public String getB_type() {
        return b_type;
    }

    public void setB_type(String b_type) {
        this.b_type = b_type;
    }
}
